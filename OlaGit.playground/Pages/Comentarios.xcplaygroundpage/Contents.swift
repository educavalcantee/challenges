/*
 Git: é um sistema de controle de versionamento. Registra quaisquer alterações feitas em um código, e permite acessar versões anteriores caso desejado, economizando tempo e recursos.
 
 Github: Plataforma online para o gerenciamento de códigos e ambiente de colaboração entre devs. Nele é possível criar repositórios, e neles, hospedar nossos projetos.
 
 GItFlow: Modelo estratégico de fluxo de trabalho que auxilia na organização do versionamento de códigos. Tem por objetivo melhorar a organizaçao das branches dentro dos repositórios de forma a dar mais fluidez ao processo.
 
 Comandos Git:
 
 git init - cria um novo repositório ou reinicia um já existente.
 
 git add - adiciona um arquivo alterado para a stage area, isto [e, o prepara para ser vinculado a um commit.
 
 git commit - move os arquivos da stage area para o repositorio local (salva as alterações).
 
 git branch - cria uma nova branch no projeto.
 
 git checkout - troca entre uma versáo e outra do projeto (alterna entre as branches).
 
 git pull - traz um arquivo do repositorio remoto para o local
 
 git push - envia arquivos do repositorio local para o remoto.
 */
